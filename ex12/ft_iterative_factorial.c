int		ft_iterative_factorial(int nb)
{
	int v;

	v = 0;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	v = nb;
	while (v > 1)
	{
		nb = nb * (v - 1);
		v--;
	}
	return (nb);
}
